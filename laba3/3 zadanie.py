def count_letters(text):  # Функция подсчитывает количество каждой буквы в тексте
    letter_count = {}  # Создаем пустой словарь для хранения количества букв

    for symbol in text.lower():  # Переводим текст в нижний регистр и перебираем каждый символ
        if symbol.isalpha():  # Проверяем, является ли символ буквой
            if symbol in letter_count:  # Если буква уже есть в словаре
                letter_count[symbol] += 1  # Увеличиваем счетчик на 1
            else:  # Если буквы еще нет в словаре
                letter_count[symbol] = 1  # Добавляем букву в словарь со значением 1

    return letter_count  # Возвращаем словарь с количеством букв


def calculate_frequency(letter_count):  # Функция вычисляет частоту каждой буквы
    total_letters = sum(letter_count.values())  # Находим общее количество букв
    frequency = {}  # Создаем пустой словарь для частот

    for letter, count in letter_count.items():  # Перебираем буквы и их количество
        frequency[letter] = count / total_letters  # Вычисляем частоту буквы

    return frequency  # Возвращаем словарь с частотами





main_str = """
У лукоморья дуб зелёный;
Златая цепь на дубе том:
И днём и ночью кот учёный
Всё ходит по цепи кругом;
Идёт направо — песнь заводит,
Налево — сказку говорит.
Там чудеса: там леший бродит,
Русалка на ветвях сидит;
Там на неведомых дорожках
Следы невиданных зверей;
Избушка там на курьих ножках
Стоит без окон, без дверей;
Там лес и дол видений полны;
Там о заре прихлынут волны
На брег песчаный и пустой,
И тридцать витязей прекрасных
Чредой из вод выходят ясных,
И с ними дядька их морской;
Там королевич мимоходом
Пленяет грозного царя;
Там в облаках перед народом
Через леса, через моря
Колдун несёт богатыря;
В темнице там царевна тужит,
А бурый волк ей верно служит;
Там ступа с Бабою Ягой
Идёт, бредёт сама собой,
Там царь Кащей над златом чахнет;
Там русский дух… там Русью пахнет!
И там я был, и мёд я пил;
У моря видел дуб зелёный;
Под ним сидел, и кот учёный
Свои мне сказки говорил.
"""

letters = count_letters(main_str)  # Подсчитываем количество букв
frequencies = calculate_frequency(letters)  # Вычисляем частоты

for letter, freq in frequencies.items():  # Перебираем буквы и их частоты
    print(f"{letter}: {freq:.2f}")  # Печатаем букву и частоту, округленную до 2 знаков
